# Módulos em Python são basicamente bibliotecas, que podemos adicionar funcionalidades extras ao programa.
# Para isso, usamos o comando import nas primeiras linhas do programa

# import math - Importa toda a biblioteca math, embora talvez não precisemos de todos os seus componentes.
# from math import sqrt - Importa apenas a funcionalidade sqrt da biblioteca math.
# from math import ceil, floor - Importa as funcionalidades ceil e floor da biblioteca math

import math

a = math.ceil(5.678)
print(a)
b = math.sqrt(81)
print(b)

